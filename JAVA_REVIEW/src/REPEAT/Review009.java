package REPEAT;

public class Review009
{
	public static void main(String[] args)
	{
		//① 2중첩 *2
		System.out.println("① 2중첩 *2");
		// 2 ~ 5단
		for (int i = 1; i < 9; i++)
		{
			for (int j = 2; j <= 5; j++)
			{
				System.out.printf("%2d x %2d = %2d  ",j,i,(j*i));
			}
			System.out.println();
		}
		System.out.println();
		// 6 ~ 9단
		for (int i = 1; i < 9; i++)
		{
			for (int j = 6; j <= 9; j++)
			{
				System.out.printf("%2d x %2d = %2d  ",j,i,(j*i));
			}
			System.out.println();
		}
		
		System.out.println();
		//②3중첩 *1
		System.out.println("②3중첩 *1");
		
		//※ 여기서 내가 어려워 했던 점: 첫문단과 두번째 문단의 시작지점을 구분하는 것.
		for (int i = 1; i <= 5; i+=4)
		{
			for (int j = 1; j <= 9; j++)
			{
				for (int h = i+1; h <= i+4; h++)
				{
					System.out.printf("%2d x %2d = %2d  ",h,j,(h*j));
					
				}
				System.out.println();
			}
			System.out.println();
			
		}
		
	}
}
